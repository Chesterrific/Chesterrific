function openNav() {
    var sidebar = document.getElementById("rightnav");
    sidebar.style.width = "100%";
}

function closeNav() {
    var sidebar = document.getElementById("rightnav");
    sidebar.style.width = "0%";
}

function openProjects(){    
    var sidebar = document.getElementById("projects");
    sidebar.style.width = "25%";
}

function closeProjects(){
    var sidebar = document.getElementById("projects");
    sidebar.style.width = "0%";
}